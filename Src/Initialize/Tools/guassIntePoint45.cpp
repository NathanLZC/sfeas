#include "Initialize/Tools/guassIntePoint45.h"

void guassIntePoint45(int nIntPt, dMatrix& XP, double* WP) {
	// calculate guass integral point
	switch (nIntPt) {
	case 1:				// 1 point hammer integral(3D4 nodes)
		XP(0, 0) = 0.25;
		XP(1, 0) = 0.25;
		XP(2, 0) = 0.25;

		WP[0] = 0.1666666666667;
		break;
	case 6:				// 6 points guass integral(3D6 nodes)
		XP(0, 0) = 0.1666666666667;
		XP(1, 0) = 0.1666666666667;
		XP(2, 0) = -0.5773502691896;

		XP(0, 1) = 0.6666666666667;
		XP(1, 1) = 0.1666666666667;
		XP(2, 1) = -0.5773502691896;

		XP(0, 2) = 0.1666666666667;
		XP(1, 2) = 0.6666666666667;
		XP(2, 2) = -0.5773502691896;

		XP(0, 3) = 0.1666666666667;
		XP(1, 3) = 0.1666666666667;
		XP(2, 3) = 0.5773502691896;

		XP(0, 4) = 0.6666666666667;
		XP(1, 4) = 0.1666666666667;
		XP(2, 4) = 0.5773502691896;

		XP(0, 5) = 0.1666666666667;
		XP(1, 5) = 0.6666666666667;
		XP(2, 5) = 0.5773502691896;

		for (int i = 0; i < 6; i++) { WP[i] = 0.1666666666667; }
		break;
	case 8:				// 8 points guass integral(3D8 nodes)
		XP(0, 0) = -0.5773502691896;
		XP(1, 0) = -0.5773502691896;
		XP(2, 0) = -0.5773502691896;

		XP(0, 1) = 0.5773502691896;
		XP(1, 1) = -0.5773502691896;
		XP(2, 1) = -0.5773502691896;

		XP(0, 2) = 0.5773502691896;
		XP(1, 2) = 0.5773502691896;
		XP(2, 2) = -0.5773502691896;

		XP(0, 3) = -0.5773502691896;
		XP(1, 3) = 0.5773502691896;
		XP(2, 3) = -0.5773502691896;

		XP(0, 4) = -0.5773502691896;
		XP(1, 4) = -0.5773502691896;
		XP(2, 4) = 0.5773502691896;

		XP(0, 5) = 0.5773502691896;
		XP(1, 5) = -0.5773502691896;
		XP(2, 5) = 0.5773502691896;

		XP(0, 6) = 0.5773502691896;
		XP(1, 6) = 0.5773502691896;
		XP(2, 6) = 0.5773502691896;

		XP(0, 7) = -0.5773502691896;
		XP(1, 7) = 0.5773502691896;
		XP(2, 7) = 0.5773502691896;

		for (int i = 0; i < 8; i++) { WP[i] = 1.0; }
		break;
	default:
		break;
	}

	return;
}

